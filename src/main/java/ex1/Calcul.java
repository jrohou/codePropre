package ex1;

public class Calcul {

	/** Method creates a somme
	 * @param a is an int
	 * @param b is an int
	 * @return a somme between a and b
	 */
	public int get1(int a, int b){
		return a+b;
	}
	
	/** Method creates a difference
	 * @param a is an int
	 * @param b is an int
	 * @return a difference between a and b
	 */
	public int get2(int a, int b){
		return a-b;
	}
}
